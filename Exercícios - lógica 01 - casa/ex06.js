var readline = require('readline-sync')
var letra = readline.question('Digite uma letra: ')



if(letra == 'a') {
    console.log('Você digitou uma Vogal')
}else if (letra == 'e') {
    console.log('Você digitou uma Vogal')
}else if (letra == 'i') {
    console.log('Você digitou uma Vogal')
}else if (letra == 'o') {
    console.log('Você digitou uma Vogal')
}else if (letra == 'u') {
    console.log('Você digitou uma vogal')

}else {
console.log('Você digitou uma Consoante')
}